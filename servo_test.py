# ==============================================================
#  サーボモーターを動かしてみよう！
#  対象: ESP32 WROOM-32E + FS90 サーボ（GPIO16）
# ==============================================================

# まず、必要な「道具箱（ライブラリ）」を読み込む
from machine import Pin, PWM   # ピンを操作するライブラリ
import time                    # 時間を操作するライブラリ

# ---------------------------------------------------
# サーボの準備
# ---------------------------------------------------
# PWM = 「電気信号のON/OFFの速さ」を使って
#        モーターの角度をコントロールする方法

servo = PWM(Pin(16))   # 16番ピンにサーボをつなぐ
servo.freq(50)         # 1秒間に50回信号を送る（サーボのお約束）

# ---------------------------------------------------
# 角度を変える関数
# ---------------------------------------------------
# 「関数」＝ よく使う命令をひとまとめにして名前をつけたもの
# set_angle(角度) と書くと、サーボが動く！

def set_angle(angle):
    # 角度（0〜180）を電気信号の長さ（ナノ秒）に計算して変換する
    # 0度 → 500000ナノ秒（0.5ミリ秒）
    # 90度 → 1500000ナノ秒（1.5ミリ秒）
    # 180度 → 2500000ナノ秒（2.5ミリ秒）
    pulse = int(500_000 + (angle / 180) * 2_000_000)
    servo.duty_ns(pulse)

# ---------------------------------------------------
# メインプログラム
# ---------------------------------------------------

print("サーボテスト、スタート！")

# ループ = 同じことをずっと繰り返す
while True:

    print("→ 45度")
    set_angle(45)       # 翼を上げる
    time.sleep(1)       # 1秒待つ

    print("→ 90度（まんなか）")
    set_angle(90)       # 翼を真ん中に戻す
    time.sleep(1)

    print("→ 135度")
    set_angle(135)      # 翼を下げる
    time.sleep(1)

    print("→ 90度（まんなか）")
    set_angle(90)
    time.sleep(1)

    print("--- 1周おわり！また最初から ---")
